export function t(lang, key, params = {}) {
  let str = translations[lang]?.[key] || translations.en[key] || key;
  Object.entries(params).forEach(([k, v]) => {
    str = str.replace(new RegExp(`\\{${k}\\}`, "g"), v);
  });
  return str;
}

